    private void alterarFuncionarios() {
        String nome, RG, CPF, endereco, sexo, mail, telefone;
        int id_funcionario;

        id_funcionario = Integer.parseInt(txtID.getText());
        nome = txtNome.getText();
        RG = txtRG.getText();
        CPF = txtCPF.getText();
        endereco = txtEndereco.getText();
        sexo = txtSexo.getText();
        mail = txtMail.getText();
        telefone = txtTelefone.getText();

        FuncionarioDTO funcionariodto = new FuncionarioDTO();
        funcionariodto.setId_funcionario(id_funcionario);
        funcionariodto.setNome_func(nome);
        funcionariodto.setRG_func(RG);
        funcionariodto.setCPF_func(CPF);
        funcionariodto.setEndereco_func(endereco);
        funcionariodto.setSexo_func(sexo);
        funcionariodto.setSexo_func(sexo);
        funcionariodto.setMail_func(mail);
        funcionariodto.setTelefone_func(telefone);

        FuncionarioDAO funcionariodao = new FuncionarioDAO();
        funcionariodao.alterarFuncionario(funcionariodto);

    }

    private void deletarFuncionario() {
        int id_funcionario;

        id_funcionario = Integer.parseInt(txtID.getText());

        FuncionarioDTO funcionariodto = new FuncionarioDTO();
        funcionariodto.setId_funcionario(id_funcionario);

        FuncionarioDAO funcionariodao = new FuncionarioDAO();
        funcionariodao.deletarFuncionario(funcionariodto);
    }
