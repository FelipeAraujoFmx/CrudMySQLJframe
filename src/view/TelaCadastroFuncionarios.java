    private void listarValoresFuncionarios() {
        try {
            FuncionarioDAO funcionariodao = new FuncionarioDAO();

            DefaultTableModel model = (DefaultTableModel) tableFuncionarios.getModel();
            model.setNumRows(0);

            ArrayList<FuncionarioDTO> lista = funcionariodao.listarFuncionario();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getId_funcionario(),
                    lista.get(num).getNome_func(),
                    lista.get(num).getRG_func(),
                    lista.get(num).getCPF_func(),
                    lista.get(num).getEndereco_func(),
                    lista.get(num).getSexo_func(),
                    lista.get(num).getMail_func(),
                    lista.get(num).getTelefone_func()
                });
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, "Listar valores VIEW" + erro);
        }
    }

    private void limparCampos() {
        txtNome.setText("");
        txtRG.setText("");
        txtCPF.setText("");
        txtEndereco.setText("");
        txtSexo.setText("");
        txtMail.setText("");
        txtTelefone.setText("");
        txtID.setText("");
    }

    private void carregarCampos() {
        int setar = tableFuncionarios.getSelectedRow();

        txtID.setText(tableFuncionarios.getModel().getValueAt(setar, 0).toString());
        txtNome.setText(tableFuncionarios.getModel().getValueAt(setar, 1).toString());
        txtRG.setText(tableFuncionarios.getModel().getValueAt(setar, 2).toString());
        txtCPF.setText(tableFuncionarios.getModel().getValueAt(setar, 3).toString());
        txtEndereco.setText(tableFuncionarios.getModel().getValueAt(setar, 4).toString());
        txtSexo.setText(tableFuncionarios.getModel().getValueAt(setar, 5).toString());
        txtMail.setText(tableFuncionarios.getModel().getValueAt(setar, 6).toString());
        txtTelefone.setText(tableFuncionarios.getModel().getValueAt(setar, 7).toString());
    }

